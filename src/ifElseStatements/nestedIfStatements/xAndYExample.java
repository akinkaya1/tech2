public class xAndYExample {
    public static void main(String[] args) {
        int x = 30;
        int y = 11;
        if(x == 30) {
            if(y == 10) {
                System.out.println("X = 30 and Y = 10");
            }
        }

        System.out.println("End of the program");
    }
}
