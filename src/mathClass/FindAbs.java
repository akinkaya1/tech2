package mathClass;

public class FindAbs {
    public static void main(String[] args) {
        /*
        Math.abs() method always returns positive value of given number
        this method takes ONLY 1 number
        When we need to measure distance, we will use this method
         */

        int number1 = Math.abs(5-10); // 5
        int number2 = Math.abs(10-5); // 5

        System.out.println(number1);
        System.out.println(number2);

    }
}
