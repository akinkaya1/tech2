package primitives;

public class ReferenceTypesString {
    public static void main(String[] args) {

        //To store sequence of characters, we use String

        String name; // declaration of name variable

        name = "Lionel"; // assignment

        System.out.println(name); // Lionel

        String lastName = "Messi";

        System.out.println(lastName); // Messi
    }
}
