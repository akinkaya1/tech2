package primitives;

public class Char {
    public static void main(String[] args) {

        char c1 = 'A'; // declaring and assigning in the same line

        System.out.println(c1); // A

        char c2 = 'B';

        System.out.println(c2); // B

        char c3; // declaration
        c3 = '%'; // assignment

        c3 = '$'; // re-assignment

        System.out.println(c3); // $

        //declare a new char variable which holds zero and print it
        char zero = '0';
        System.out.println(zero);
    }
}
