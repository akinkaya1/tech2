package string;

public class UnderstandingString {
    public static void main(String[] args) {
        String greeting = "Hello";

        System.out.println(greeting); // Hello

        //One way is to declare first and initialize later
        String schoolName; // declaring
        schoolName = "TechGlobal"; // initializing or assigning

        System.out.println(schoolName); // TechGlobal

        //Another way is to declare and initialize in the same statement
        String schoolAddress = "2860 S River Rd Des Plaines";

        System.out.println(schoolAddress);
    }
}
