package operators.arithmeticOperators;

public class Division {
    public static void main(String[] args) {
        int d1 = 5 / 2; // 2
        double d2 = (double)5 / 2; // 2.5 - casting

        System.out.println("Division of 5 by 2 with int is = " + d1);
        System.out.println("Division of 5 by 2 with double is = " + d2);

    }
}
